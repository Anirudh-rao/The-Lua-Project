function DataTypes()
    print(type(nil)) --nil
    print(type(true)) --boolean
    print(type(10.4*3)) --number
    print(type("Hello World")) --string
    print(type(print)) --function
    print(type(io.stdin)) --userdata
    print(type(type)) --function
    print(type({})) --Table
    print(type(X))    -- string
end